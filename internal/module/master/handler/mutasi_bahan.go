func (h *MasterHandler) getMutasiBahans(c *fiber.Ctx) error {
	var (
		req = new(entity.GetMutasiBahansReq)
		v   = adapter.Adapters.Validator
	)

	if err := c.QueryParser(req); err != nil {
		log.Warn().Err(err).Msg("handler::getMutasiBahans - failed to parse request")
		return c.Status(fiber.StatusBadRequest).JSON(response.Error(err))
	}
	log.Debug().Any("req", req).Msg("Parsed Query")

	req.SetDefault()

	if err := v.Validate(req); err != nil {
		log.Warn().Err(err).Any("req", req).Msg("handler::getMutasiBahans - invalid request")
		code, errs := errmsg.Errors(err, req)
		return c.Status(code).JSON(response.Error(errs))
	}

	resp, err := h.service.GetMutasiBahans(c.Context(), req)
	if err != nil {
		code, errs := errmsg.Errors[error](err)
		return c.Status(code).JSON(response.Error(errs))
	}

	return c.JSON(response.Success(resp, ""))
}

